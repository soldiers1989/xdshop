public class OrderDetailVO {

	private String detailId;

	private String branchName;

	private Integer wireNum;

	private Integer wirelessNum;

	private String name;

	private String mobile1;

	private String address;

	private String remark;

	/**
	 * @return detailId
	 */
	public String getDetailId() {
		return detailId;
	}

	/**
	 * @param detailId
	 *            要设置的 detailId
	 */
	public void setDetailId(String detailId) {
		this.detailId = detailId;
	}

	/**
	 * @return branchName
	 */
	public String getBranchName() {
		return branchName;
	}

	/**
	 * @param branchName
	 *            要设置的 branchName
	 */
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}

	/**
	 * @return wireNum
	 */
	public Integer getWireNum() {
		return wireNum;
	}

	/**
	 * @param wireNum
	 *            要设置的 wireNum
	 */
	public void setWireNum(Integer wireNum) {
		this.wireNum = wireNum;
	}

	/**
	 * @return wirelessNum
	 */
	public Integer getWirelessNum() {
		return wirelessNum;
	}

	/**
	 * @param wirelessNum
	 *            要设置的 wirelessNum
	 */
	public void setWirelessNum(Integer wirelessNum) {
		this.wirelessNum = wirelessNum;
	}

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            要设置的 name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return mobile1
	 */
	public String getMobile1() {
		return mobile1;
	}

	/**
	 * @param mobile1
	 *            要设置的 mobile1
	 */
	public void setMobile1(String mobile1) {
		this.mobile1 = mobile1;
	}

	/**
	 * @return address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address
	 *            要设置的 address
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return remark
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 * @param remark
	 *            要设置的 remark
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}

}
