package com.pujjr.gps.dal.domain;

public class HisBeanMap {
    private String id;

    private String className;

    private String classCname;

    private String tableName;

    private String tableCname;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassCname() {
        return classCname;
    }

    public void setClassCname(String classCname) {
        this.classCname = classCname;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getTableCname() {
        return tableCname;
    }

    public void setTableCname(String tableCname) {
        this.tableCname = tableCname;
    }
}